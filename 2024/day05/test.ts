import { assertEquals } from "@std/assert";
import { read_text } from "../util.ts";
import { parse_input, solve_one, solve_two } from "./impl.ts";

Deno.test("answer_one", () => {
  const input = read_text(`
    47|53
    97|13
    97|61
    97|47
    75|29
    61|13
    75|53
    29|13
    97|29
    53|29
    61|53
    97|53
    61|29
    47|13
    75|47
    97|75
    47|61
    75|61
    47|29
    75|13
    53|13

    75,47,61,53,29
    97,61,53,29,13
    75,29,13
    75,97,47,61,53
    61,13,29
    97,13,75,29,47
  `);

  assertEquals(solve_one(parse_input(input)), 143);
});

